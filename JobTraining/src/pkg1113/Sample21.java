public class Sample21 {
	public static void main(String[] args) {
		int value = 1;
		value = 2;
		value = 3;
		value = 4;
		value = 5;
		
		switch(value) {
			case 1:
				System.out.println("大吉です");
				break;
			case 2:
				System.out.println("吉です");
				break;
			case 3:
				System.out.println("中吉です");
				break;
			case 4:
				System.out.println("末吉です");
				break;
			case 5:
				System.out.println("凶です");
				break;
		}
	}
}
